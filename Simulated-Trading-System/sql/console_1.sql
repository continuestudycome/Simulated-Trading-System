CREATE DATABASE IF NOT EXISTS trading;
USE trading;

CREATE TABLE `account` (
  `id` bigint PRIMARY KEY AUTO_INCREMENT,
  `user_id` bigint NOT NULL COMMENT '用户ID',
  `balance` decimal(20,2) NOT NULL DEFAULT '0.00' COMMENT '可用余额',
  `frozen` decimal(20,2) NOT NULL DEFAULT '0.00' COMMENT '冻结金额（下单时冻结的资金）',
  `version` int NOT NULL DEFAULT '0' COMMENT '乐观锁版本号，用于并发控制',
  INDEX `idx_user_id` (`user_id`) COMMENT '用户ID索引'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT '资金账户表';
/**
    设计亮点：
        1.使用 version 字段实现乐观锁，防止并发操作时资金重复扣减
        2.balance 和 frozen 分离，便于计算总资产 = balance + frozen
        3.对 user_id 建立索引，加快按用户查询速度
 */

CREATE TABLE `position` (
  `id` bigint PRIMARY KEY AUTO_INCREMENT,
  `user_id` bigint NOT NULL COMMENT '用户ID',
  `stock_code` varchar(10) NOT NULL COMMENT '股票代码（如''600001''）',
  `quantity` int NOT NULL COMMENT '持仓数量（股数）',
  `cost_price` decimal(10,2) NOT NULL COMMENT '成本价（持仓均价）',
  UNIQUE KEY `uk_user_stock` (`user_id`, `stock_code`)  COMMENT '用户股票组合唯一索引'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT '持仓表';
/**
    设计亮点：
        1.联合唯一索引 uk_user_stock 确保同一用户对同一股票只有一条持仓记录
        2.成本价字段支持计算浮动盈亏
 */

CREATE TABLE `order` (
  `id` bigint PRIMARY KEY AUTO_INCREMENT,
  `user_id` bigint NOT NULL,
  `stock_code` varchar(10) NOT NULL COMMENT '股票代码（如''600001''）',
  `type` tinyint NOT NULL COMMENT '订单类型，1买 2卖',
  `price` decimal(10,2) NOT NULL COMMENT '委托价格',
  `quantity` int NOT NULL COMMENT '委托数量',
  `status` tinyint NOT NULL COMMENT '订单状态，0待处理 1已成交 2已撤单',
  `create_time` datetime NOT NULL COMMENT '下单时间',
  INDEX `idx_user_id` (`user_id`) COMMENT '用户ID索引'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT '订单表';



